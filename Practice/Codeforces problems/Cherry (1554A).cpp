void solve() {
    int n; cin >> n;
    vector<ll> nums(n);
    for(auto &x: nums) cin >> x;
    ll sol = 0;
    for(int i=0; i<n-1; i++) {
        sol = max({sol, nums[i]*nums[i+1]});
    }
    cout << sol << endl;
}

