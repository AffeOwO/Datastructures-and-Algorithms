void solve() {
    int n; cin >> n;
    
    // since n is odd, there is one cell in the middle of the board where the sum of distances is the smallest for all
    ll sol = 0;
    for(ll i=1; i<=n/2; i++) {
        sol += (8*i*i);
    }
    cout << sol << endl;
}

